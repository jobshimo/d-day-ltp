// @ts-check
/**
 * extract-narration.mjs
 *
 * Reads ALL_MODULES from the content library, collects every narratable block
 * (type === 'prose' || type === 'rule-callout'), computes a content-addressed
 * hash, and writes two artifacts:
 *
 *   - tools/tts/narration-manifest.json
 *   - libs/content/src/narration.generated.ts
 *
 * Run with:
 *   node --experimental-strip-types tools/tts/extract-narration.mjs
 *
 * Requires Node 22+ (--experimental-strip-types) or fall back to:
 *   npx -y tsx tools/tts/extract-narration.mjs
 */

import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = resolve(__dirname, '..', '..');

// Load narration config (single source of truth for voice)
const configPath = resolve(__dirname, 'narration.config.json');
const config = JSON.parse(readFileSync(configPath, 'utf8'));
const { voice } = config;

// Use createRequire to load the content lib (compiled via tsconfigPaths at runtime)
// We load via the direct file path to avoid needing the full Nx build pipeline.
const require = createRequire(import.meta.url);

// Resolve the content entry point from tsconfig.base.json paths
const contentEntry = resolve(repoRoot, 'libs/content/src/index.ts');
// Convert Windows absolute path to a file:// URL so the ESM loader accepts it
const contentUrl = new URL('file://' + contentEntry.replace(/\\/g, '/')).href;

// Since we cannot import raw TS in plain node without tsx, check if tsx is available.
// If not, try importing directly (Node 22 type-strip mode).
let ALL_MODULES;
try {
  // Node 22 --experimental-strip-types: import TS file directly via file:// URL
  const mod = await import(contentUrl);
  ALL_MODULES = mod.ALL_MODULES;
} catch (err) {
  console.error('Failed to import content lib directly:', err.message);
  console.error('Try running with: npx -y tsx tools/tts/extract-narration.mjs');
  process.exit(1);
}

if (!ALL_MODULES) {
  console.error('ALL_MODULES is undefined after import');
  process.exit(1);
}

/** @param {string} text @param {string} v @returns {string} */
function computeHash(text, v) {
  return createHash('sha1')
    .update(v + '\n' + text)
    .digest('hex')
    .slice(0, 12);
}

/** @type {Array<{key: string, lessonId: string, blockIndex: number, text: string, file: string, voice: string}>} */
const manifest = [];

for (const mod of ALL_MODULES) {
  for (const lesson of mod.lessons) {
    for (let i = 0; i < lesson.blocks.length; i++) {
      const block = lesson.blocks[i];
      // Narratable block types: prose and rule-callout
      if (block.type !== 'prose' && block.type !== 'rule-callout') continue;
      const text = block.content.trim();
      if (!text) continue;

      const key = `${lesson.id}#${i}`;
      const hash = computeHash(text, voice);
      const file = `${hash}.mp3`;

      manifest.push({ key, lessonId: lesson.id, blockIndex: i, text, file, voice });
    }
  }
}

// Write manifest
const manifestPath = resolve(__dirname, 'narration-manifest.json');
writeFileSync(manifestPath, JSON.stringify(manifest, null, 2) + '\n', 'utf8');
console.log(`Wrote ${manifestPath} (${manifest.length} entries)`);

// Write narration.generated.ts
const tsLines = [
  '// GENERATED by tools/tts/extract-narration.mjs — do not edit by hand.',
  '',
  '/**',
  ' * Maps each narratable block key ("<lessonId>#<blockIndex>") to its',
  ' * pre-generated MP3 asset path relative to the app root.',
  ' */',
  'export const NARRATION: Record<string, string> = {',
];

for (const entry of manifest) {
  tsLines.push(`  ${JSON.stringify(entry.key)}: ${JSON.stringify('assets/audio/' + entry.file)},`);
}
tsLines.push('};');
tsLines.push('');
tsLines.push('/**');
tsLines.push(' * Returns the audio asset path for a given lesson block, or undefined if');
tsLines.push(' * the block is non-narratable or no audio has been generated for it.');
tsLines.push(' *');
tsLines.push(' * @param lessonId  - the lesson id (e.g. "lesson-1-1")');
tsLines.push(' * @param blockIndex - the zero-based index into lesson.blocks');
tsLines.push(' */');
tsLines.push('export function getBlockNarration(lessonId: string, blockIndex: number): string | undefined {');
tsLines.push('  return NARRATION[`${lessonId}#${blockIndex}`];');
tsLines.push('}');
tsLines.push('');

const tsContent = tsLines.join('\n');
const generatedTsPath = resolve(repoRoot, 'libs/content/src/narration.generated.ts');
writeFileSync(generatedTsPath, tsContent, 'utf8');
console.log(`Wrote ${generatedTsPath}`);

console.log(`Total narratable blocks: ${manifest.length}`);
